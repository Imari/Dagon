State::~State() {
  // Nothing to do here
}

////////////////////////////////////////////////////////////
// Implementation
////////////////////////////////////////////////////////////

int State::current() {
  return _current;
}

int State::previous() {
  return _previous;
}

void State::set(int theState) {
  _previous = _current;
  _current = theState;
}

void State::setPrevious() {
  int auxState = _current;
  
  _current = _previous;
  _previous = auxState;
}
  
